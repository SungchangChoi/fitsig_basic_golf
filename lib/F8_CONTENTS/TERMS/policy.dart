<P CLASS=HStyle0><SPAN STYLE='font-family:"맑은 고딕"'>①계정 이용 제한</SPAN></P>

<P CLASS=HStyle0><SPAN STYLE='font-family:"맑은 고딕"'>- 계정의 생성이나 이용 과정에서 관련 법령, 관련 약관 또는 운영정책 위반 사항이 발견된 경우, 계정의 생성·이용·탈퇴가 제한될 수 있습니다.</SPAN></P>

<P CLASS=HStyle0><SPAN STYLE='font-family:"맑은 고딕"'>- 계정을 비정상적으로 생성·이용·탈퇴하려는 시도가 발견된 경우, 해당 계정의 이용 및 탈퇴를 제한하거나, 해당 계정의 비정상적 생성이 계속 될 수 없도록 계정 생성이 제한 될 수도 있으며, 필요할 경우 해당 계정을 삭제하고 재가입을 제한하는 등의 적절한 제한을 할 수 있습니다.</SPAN></P>

